/// Returns the localized country name for the given [isoCode] and [languageCode].
///
/// Falls back to the Russian name (template language), then to the [isoCode] itself.
String localizedCountryName(String isoCode, String languageCode) {
  final map = languageCode == 'en' ? kCountryNamesEn : kCountryNamesRu;
  return map[isoCode] ?? kCountryNamesRu[isoCode] ?? isoCode;
}
